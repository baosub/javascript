// Ejemplo de spread operator con arrays
const numeros1 = [1, 2, 3];
const numeros2 = [...numeros1, 4, 5, 6];
console.log(numeros2); // [1, 2, 3, 4, 5, 6]

// Ejemplo de spread operator con objetos
const objeto1 = { a: 1, b: 2 };
const objeto2 = { ...objeto1, c: 3 };
console.log(objeto2); // { a: 1, b: 2, c: 3 }
