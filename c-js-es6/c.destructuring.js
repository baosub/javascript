// Ejemplo de destructuring con objetos
const persona = { nombre: "Ana", edad: 25 };
const { nombre, edad } = persona;
console.log(nombre); // Ana
console.log(edad); // 25

// Ejemplo de destructuring con arrays
const numeros = [1, 2, 3];
const [uno, dos, tres] = numeros;
console.log(uno); // 1
console.log(dos); // 2
console.log(tres); // 3
